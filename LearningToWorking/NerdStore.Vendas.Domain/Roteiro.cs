                SE O COMMAND É VALIDO
                SE O PEDIDO EXISTE
                SE O ITEM DO PEDIDO EXISTE


            NA ALTERAÇÃO DE ESTADO DO PEDIDO:
                
                DEVE SER FEITA VIA REPOSITORIO
                DEVE ENVIAR UM EVENTO

